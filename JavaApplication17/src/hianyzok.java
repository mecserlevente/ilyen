public class hianyzok {
    
    String veznev;
    String keresztnev;
    String azonosito;

    public hianyzok(String veznev, String keresztnev, String azonosito) {
        this.veznev = veznev;
        this.keresztnev = keresztnev;
        this.azonosito = azonosito;
    }

    public String getVeznev() {
        return veznev;
    }

    public String getKeresztnev() {
        return keresztnev;
    }

    public String getAzonosito() {
        return azonosito;
    }
    
    
}
