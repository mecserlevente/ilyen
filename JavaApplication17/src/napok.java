public class napok {
 
    String hesteg;
    int honap;
    int nap;

    public napok(String hesteg, int honap, int nap) {
        this.hesteg = hesteg;
        this.honap = honap;
        this.nap = nap;
    }

    public String getHesteg() {
        return hesteg;
    }

    public int getHonap() {
        return honap;
    }

    public int getNap() {
        return nap;
    }
    
    
}
